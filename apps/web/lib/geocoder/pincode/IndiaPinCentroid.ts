export function getIndiaPinCentroid(pincode: string): { lat: number; lng: number } | null {
  const digits = pincode.replace(/\D/g, '');
  if (digits.length < 3) return null;

  const prefix3 = parseInt(digits.slice(0, 3), 10);
  if (PIN3_CENTROIDS[prefix3]) {
    const [lat, lng] = PIN3_CENTROIDS[prefix3];
    return { lat, lng };
  }

  const zone = parseInt(digits[0], 10);
  if (ZONE_FALLBACKS[zone]) {
    const [lat, lng] = ZONE_FALLBACKS[zone];
    return { lat, lng };
  }

  return null;
}
